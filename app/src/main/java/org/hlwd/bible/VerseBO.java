
package org.hlwd.bible;

class VerseBO
{
    int id;
    String bbName;
    String bName;
    String bsName;
    int bNumber;
    int cNumber;
    int vNumber;
    String vText;
    int mark;

    VerseBO()
    {
        this.id = -1;
        this.bbName = "";
        this.bName =  "";
        this.bsName = "";
        this.bNumber = -1;
        this.cNumber = -1;
        this.vNumber = -1;
        this.vText = "";
        this.mark = 0;
    }
}
